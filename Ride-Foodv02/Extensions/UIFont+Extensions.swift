import UIKit

extension UIFont {
    
    static func SFUIDisplayLight(size:CGFloat) -> UIFont? {
        return UIFont(name: "SFUIDisplay-Light", size: size)
    }
    
    static func SFUIDisplayRegular(size:CGFloat) -> UIFont? {
        return UIFont(name: "SFUIDisplay-Regular", size: size)
    }
    
    static func SFUIDisplaySemibold(size:CGFloat) -> UIFont? {
        return UIFont(name: "SFUIDisplay-Semibold", size: size)
    }
    
    static func SFUIDisplayBold(size:CGFloat) -> UIFont? {
        return UIFont(name: "SFUIDisplay-Bold", size: size)
    }
    
}
